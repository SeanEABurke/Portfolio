    private void submitBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBTNActionPerformed
        // TODO add your handling code here:
        try {
            if (!isDeanInput.isSelected()) {
                if (genderBTNM.isSelected()) {
                    gender = 'M';
                }
                if (genderBTNF.isSelected()) {
                    gender = 'F';
                }
                //Creates new teacher
                Teacher newTeacher = new Teacher(Integer.parseInt(IDinput.getText()), nameInput.getText(), bdayInput.getText(), addressInput.getText(), gender, specialtyInput.getText(), degreeInput.getSelectedItem().toString(), Double.parseDouble(workloadInput.getText()), (deptInput.getSelectedIndex() + 11));
                //Checks for matching ID
                for (Teacher teacher : MainFrame.teacherList) {
                    if (Integer.parseInt(IDinput.getText()) == teacher.getID()) {
                        JOptionPane.showMessageDialog(null, "Error: ID already exists");
                        return;
                    }
                }
                //Checks that workload isn't too high
                if (Double.parseDouble(workloadInput.getText()) > 40) {
                    JOptionPane.showMessageDialog(null, "Error: Workload can not be over 40 hours");
                    return;
                }
                //Adds to array list in MainFrame and writes to the file
                MainFrame.teacherList.add(newTeacher);
                MainFrame.writeFileTeacher("TeacherList.txt", newTeacher);
            }
            if (isDeanInput.isSelected()) {
                Dean newDean = new Dean(Integer.parseInt(IDinput.getText()), nameInput.getText(), bdayInput.getText(), addressInput.getText(), gender, specialtyInput.getText(), degreeInput.getSelectedItem().toString(), Double.parseDouble(workloadInput.getText()), (deptInput.getSelectedIndex() + 11));
                for (Department department : MainFrame.departmentList) {
                    if (department.getDepartmentID() == (deptInput.getSelectedIndex() + 11)) {
                        if (department.getDean() != null) {
                            JOptionPane.showMessageDialog(null, "Error: this department already has a dean");
                            return;
                        }
                    }
                }
                if (Double.parseDouble(workloadInput.getText()) > 40) {
                    JOptionPane.showMessageDialog(null, "Error: Workload can not be over 40 hours");
                    return;
                }
                MainFrame.deanList.add(newDean);
                MainFrame.writeFileDean("TeacherList.txt", newDean);
            }

            MainFrame.setLists();
            this.setVisible(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: Incorrect data input");
        }
    }//GEN-LAST:event_submitBTNActionPerformed

