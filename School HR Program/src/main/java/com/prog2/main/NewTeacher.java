    //gives the next teacher/dean ID that hasn't been used
    public int nextId() {
        int nextId = 0;
        for (Teacher teacher : MainFrame.teacherList) {
            if (teacher.getID() > nextId) {
                nextId = teacher.getID();
            }
        }
        for (Teacher dean : MainFrame.deanList) {
            if (dean.getID() > nextId) {
                nextId = dean.getID();
            }
        }
        return nextId + 1;
    }

